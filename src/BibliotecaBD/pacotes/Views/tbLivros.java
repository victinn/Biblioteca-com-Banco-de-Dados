    public void limparCampos(){
        txtTitulo.setText("");
        txtPaginas.setText("");
        txtNomeAutor.setText("");
        txtEditora.setText("");
        txtAnoPublicacao.setText("");
    }
    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        Livro l = pegarCampos();
        
        try {    
            this.model.Adicionar(l);
            limparCampos();
        } catch (SQLException ex) {
            Logger.getLogger(tbLivros.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        if(JOptionPane.showConfirmDialog(this, "Deseja remover este livro?", "Remoção", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            int index = tbLivro.getSelectedRow();
            try {
                model.Remover(index);
                limparCampos();
            } catch (SQLException ex) {
                Logger.getLogger(tbLivros.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if(JOptionPane.showConfirmDialog(this, "Deseja alterar este lirvo?", "Alteração", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            int index = tbLivro.getSelectedRow();
            Livro l = pegarCampos();
            
            Livro selecionado = model.getSelectedLivro(index);
            l.setId(selecionado.getId());
            try {
                model.Alterar(l);
                limparCampos();
            } catch (SQLException ex) {
                Logger.getLogger(tbLivros.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

