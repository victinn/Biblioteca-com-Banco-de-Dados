    private Livro pegarCampos(){
        Livro l = new Livro();
        l.setTitulo(txtTitulo.getText());
        l.setPaginas(Integer.parseInt(txtPaginas.getText()));
        l.setNomeAutor(txtNomeAutor.getText());
        l.setEditora(txtEditora.getText());
        l.setGenero((String) comboGenero.getSelectedItem());
        l.setAnoPublicacao(Integer.parseInt(txtAnoPublicacao.getText()));
        
        return l;
    }
